# INFO: Метод .replace() заменяет подстроку в строке. Аргументами для метода являются старое наименование и новое.


text = "Java is my favorite programming language. Java rocks!"
update_text = text.replace("Java", "Python")
print(text)
# Вывод: Java is my favorite programming language. Java rocks!
print(update_text)
# Вывод: Python is my favorite programming language. Python rocks!