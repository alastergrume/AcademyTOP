def less_4(number):
    '''
    Пользователь вводит любое целое число. Необходимо из этого целого числа удалить все цифры 3 и 6 и
    вывести обратно на экран.
    '''
    list_items = ''
    for item in number:
        if "6" != str(item) != "3":
            list_items += item
    return f'Число без "3" и "6" = {list_items}'


print(less_4(input('Введите любое целое число - ')))

