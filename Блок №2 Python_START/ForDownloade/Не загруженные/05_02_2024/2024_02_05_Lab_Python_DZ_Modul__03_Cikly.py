def less_2():
    '''
    Подсчитать количество целых чисел в диапазоне от
    100 до 999 у которых есть две одинаковые цифры.
    '''

    list1_2 = [x for x in range(100, 1000) if len(set(str(x))) == 2]
    return f'Количество чисел, у которых есть две одинаковые цифры в промежутке от 100 до 999 {len(list1_2)}'



print(less_2())


